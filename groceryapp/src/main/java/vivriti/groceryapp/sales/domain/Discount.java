package vivriti.groceryapp.sales.domain;


public class Discount {
	
	private double amount;
	private String disc;
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public String getDisc() {
		return disc;
	}
	public void setDisc(String disc) {
		this.disc = disc;
	}

}
